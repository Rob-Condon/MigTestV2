if ( !function_exists( 'zmb_iconpicker_build_param' ) ) {
	function zmb_iconpicker_build_param( $settings = array() ) {

		$settings_default = array(
			'list_of_icons'        => array(
				esc_html__( 'Font Awesome', 'zanmb' ) => 'fontawesome',
				esc_html__( 'Open Iconic', 'zanmb' )  => 'openiconic',
				esc_html__( 'Typicons', 'zanmb' )     => 'typicons',
				esc_html__( 'Entypo', 'zanmb' )       => 'entypo',
				esc_html__( 'Linecons', 'zanmb' )     => 'linecons',
				esc_html__( 'Mono Social', 'zanmb' )  => 'monosocial',
				esc_html__( 'Simple Line', 'zanmb' )  => 'zansimpleline',
				//esc_html__( 'Flaticon', 'zanmb' )         => 'lkflaticon', // For Lucky Shop
				//esc_html__( 'Pe Icon 7 Stroke', 'zanmb' ) => 'peicon7stroke', // For Lucky Shop
			),
			'icon_type_param_name' => 'icon_type',
			'icon_chosen_heading'  => esc_html__( 'Icon', 'zanmb' ),
			'icon_desc'            => esc_html__( 'Select icon from library.', 'zanmb' ),
		);

		$settings = wp_parse_args( $settings, $settings_default );

		$icon_param = array();
		if ( !empty( $settings ) ) {
			$icon_param[] = array(
				'type'        => 'dropdown',
				'heading'     => esc_html__( 'Icon library', 'zanmb' ),
				'value'       => $settings['list_of_icons'],
				'param_name'  => $settings['icon_type_param_name'],
				'std'         => 'zansimpleline', // For Zan
				'description' => esc_html__( 'Select icon library.', 'zanmb' ),
			);
			foreach ( $settings['list_of_icons'] as $icon_name => $icon_type ) {
				$icon_param[] = array(
					'type'        => 'iconpicker',
					'heading'     => $settings['icon_chosen_heading'],
					'param_name'  => 'icon_' . esc_attr( $icon_type ), // 'icon_fontawesome', 'icon_lkflaticon'...
					'value'       => '',
					'settings'    => array(
						'emptyIcon'    => true, // default true, display an "EMPTY" icon?
						'type'         => esc_attr( $icon_type ),
						'iconsPerPage' => 2000, // default 100, how many icons per/page to display
					),
					'dependency'  => array(
						'element' => $settings['icon_type_param_name'],
						'value'   => esc_attr( $icon_type ),
					),
					'description' => $settings['icon_desc'],
				);
			}
		}

		return $icon_param;

	}
}


